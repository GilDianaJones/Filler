#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*src;
	size_t	i;

	src = (char *)s;
	i = 0;
	while (i < n)
	{
		src[i] = c;
		i++;
	}
	return ((void *)src);
}
